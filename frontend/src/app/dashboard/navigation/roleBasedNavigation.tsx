export const getNavigationByRole = (role: string): SidebarLink[] => {
  switch (role) {
    case 'VEHICLE_OWNER':
      return getVehicleOwnerNavigation();
    case 'FUEL_STATION_OWNER':
      return getFuelStationOwnerNavigation();
    case 'FUEL_STATION_OPERATOR':
      return getFuelStationOperatorNavigation();
    case 'ADMIN_USER':
      return getAdminNavigation();
    default:
      return commonNavigation;
  }
};
